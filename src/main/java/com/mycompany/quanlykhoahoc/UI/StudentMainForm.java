    private void setPagination() {
        count = studentBLL.countRecordStudent();
        if(count % 15 == 0) {
            soTrang = count / 15;
        } else {
            soTrang = count / 15 + 1;
        }
        jLabel_trang.setText("1");
        jLabel_so_trang.setText("1/" + soTrang);
    }
    
