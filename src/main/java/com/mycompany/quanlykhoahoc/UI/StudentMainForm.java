    public void populateJtableWithStudents(int trang) {
        ArrayList<Student> studentList = (ArrayList<Student>) studentBLL.LoadStudents(trang);
        
        // jtable columns
        String[] colNames = {"ID","FIRST NAME", "LAST NAME","ENROLLMENT DATE"};
        
        // jtable rows
        Object[][] rows = new Object[studentList.size()][colNames.length];
        
        for(int i = 0; i < studentList.size(); i++) {
            rows[i][0] = studentList.get(i).getPersonId();
            rows[i][1] = studentList.get(i).getFirstName();
            rows[i][2] = studentList.get(i).getLastName();
            rows[i][3] = studentList.get(i).getEnrollmentDate();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_Student.setModel(model);
    }
    
