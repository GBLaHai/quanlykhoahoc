    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed

        try {
            int id = Integer.parseInt(jTextField_ID.getText());
            Teacher teacher = teacherBLL.getTeacher(id);

            jTextField_FirstName.setText(teacher.getFirstName());
            jTextField_LastName.setText(teacher.getLastName());
            Date date_hire = null;
            try {
                date_hire = new SimpleDateFormat("yyyy-MM-dd").parse(teacher.getHireDate());
            } catch (ParseException ex) {
                Logger.getLogger(TeacherEditForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            jDateChooser_Date.setDate(date_hire);

        } catch (NumberFormatException ex) {
            //Logger.getLogger(EditMemberForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Enter the Valid Teacher ID", "Notification", 3);
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "No teacher with this id is found", "Notification", 3);
        }

    }//GEN-LAST:event_jButton_SearchActionPerformed

