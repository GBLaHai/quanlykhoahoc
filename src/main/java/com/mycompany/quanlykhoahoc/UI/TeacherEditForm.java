    private void jButton_Edit_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_ActionPerformed
        try {
            int teacherID = Integer.parseInt(jTextField_ID.getText());
            String firstName = jTextField_FirstName.getText();
            String lastName = jTextField_LastName.getText();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String hireDate = dateFormat.format(jDateChooser_Date.getDate());

            if (firstName.trim().isEmpty()) {
                jLabel_EmptyFirstName_.setVisible(true);
            } else if (lastName.trim().isEmpty()) {
                jLabel_EmptyLastName_.setVisible(true);
            } else {
                Teacher teacher = new Teacher(teacherID,firstName, lastName, hireDate);
                if (teacherBLL.updateTeacher(teacher) > 0) {
                    JOptionPane.showMessageDialog(null, "Teacher edited", "Notification", 1);
                } else {
                    JOptionPane.showMessageDialog(null, "Teacher not edited", "Notification", 2);
                };
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Enter teacher ID", "Notification", 3);
        }
    }//GEN-LAST:event_jButton_Edit_ActionPerformed

